#!/usr/bin/env python3
"""
Förbättrat Hello World-program
Detta program frågar efter ditt namn och hälsar personligen.
"""

def main():
    print("Välkommen till Hello World-programmet!")
    namn = input("Vad heter du? ")
    print(f"Hej {namn}! Välkommen till Python-världen.")
    print("Programmet avslutas nu.")

if __name__ == "__main__":
    main()
